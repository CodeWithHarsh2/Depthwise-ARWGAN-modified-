#!/bin/bash

set -e

CHECKPOINT="/kaggle/input/datasets/harshaggarwal925/epoch-100/epoch_100.pth"

OPTIONS="/kaggle/working/Depthwise-ARWGAN/runs/DEPTHWISE_ARWGAN 2026.07.15--07-29-56/options-and-config.pickle"

DATASET="/kaggle/input/datasets/ifigotin/imagenetmini-1000/imagenet-mini/val"

mkdir -p results

run_attack () {

    NAME="$1"
    NOISE="$2"

    echo "==========================================================="
    echo "Running: $NAME"
    echo "==========================================================="

    START=$(date +%s)

    python test1.py \
        -c "$CHECKPOINT" \
        -o "$OPTIONS" \
        -s "$DATASET" \
        -n "$NOISE" \
        --output-dir "results/$NAME"

    END=$(date +%s)

    echo "$NAME finished in $((END-START)) seconds"

    echo
    echo
}

############################################################
# Identity
############################################################

run_attack identity identity

############################################################
# JPEG
############################################################

run_attack jpeg jpeg

############################################################
# Quantization
############################################################

run_attack quant quant

############################################################
# Gaussian Blur
############################################################

run_attack gaussian "gaussian(5,1)"

############################################################
# Gaussian Noise
############################################################

run_attack gaussian_noise "Gaussian_noise(0,0.02)"

############################################################
# Salt & Pepper
############################################################

run_attack salt_pepper "sp(0.02)"

############################################################
# Resize
############################################################

run_attack resize50 "resize(0.5,0.5)"

############################################################
# Crop
############################################################

run_attack crop70 "crop((0.7,0.7),(0.7,0.7))"

############################################################
# Cropout
############################################################

run_attack cropout30 "cropout((0.3,0.3),(0.3,0.3))"

############################################################
# Dropout
############################################################

run_attack dropout70 "dropout(0.7,0.7)"

############################################################
# Median Filter
############################################################

run_attack median3 "Median_filter(3)"

############################################################
# Grid Crop
############################################################

run_attack gridcrop40 "grid_crop(0.4)"

echo
echo "==========================================================="
echo "ALL TESTS FINISHED"
echo "==========================================================="